  var currentLang = "en";
  var fallbackLang = "en";

  function detectLanguage() {
    var urlParams = new URLSearchParams(window.location.search);
    var urlLang = urlParams.get("lang");
    if (urlLang && translations[urlLang]) return urlLang;

    var stored = localStorage.getItem("bsm_language");
    if (stored && translations[stored]) return stored;

    var navLang = (navigator.language || navigator.userLanguage || "en").split("-")[0].toLowerCase();
    return translations[navLang] ? navLang : "en";
  }

  function t(key, fallback) {
    var dict = translations[currentLang] || translations[fallbackLang];
    if (dict && dict[key]) return dict[key];
    
    var fallbackDict = translations[fallbackLang];
    if (fallbackDict && fallbackDict[key]) return fallbackDict[key];
    
    return fallback || key;
  }

  function setLanguage(lang) {
    if (translations[lang]) {
      currentLang = lang;
      localStorage.setItem("bsm_language", lang);
      return true;
    }
    return false;
  }

  function getLanguage() {
    return currentLang;
  }

  function getAvailableLanguages() {
    return Object.keys(translations);
  }

  function init(lang) {
    if (lang && lang !== "auto" && translations[lang]) {
      currentLang = lang;
    } else {
      currentLang = detectLanguage();
    }
    localStorage.setItem("bsm_language", currentLang);
    return currentLang;
  }

  window.I18N = {
    t: t,
    setLanguage: setLanguage,
    getLanguage: getLanguage,
    getAvailableLanguages: getAvailableLanguages,
    init: init,
    detectLanguage: detectLanguage
  };

  window.t = t;


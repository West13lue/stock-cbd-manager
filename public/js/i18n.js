  init: function(lang) {
    // Restaurer depuis localStorage ou utiliser la langue passÃ©e
    var savedLang = null;
    try {
      savedLang = localStorage.getItem("stockmanager_lang");
    } catch (e) {}
    
    var targetLang = savedLang || lang || "fr";
    
    if (this.translations[targetLang]) {
      this.currentLang = targetLang;
      console.log("[i18n] Initialized with language:", targetLang);
    } else {
      this.currentLang = "fr";
      console.log("[i18n] Language not found, defaulting to fr");
    }
    return this.currentLang;
  },
  
  setLanguage: function(lang) {
    if (this.translations[lang]) {
      this.currentLang = lang;
      // Sauvegarder dans localStorage
      try {
        localStorage.setItem("stockmanager_lang", lang);
      } catch (e) {}
      console.log("[i18n] Language set to:", lang);
      return true;
    }
    console.warn("[i18n] Language not found:", lang);
    return false;
  },
  
  // Alias for compatibility
  setLang: function(lang) {
    return this.setLanguage(lang);
  },
  
  t: function(key, fallback) {
    var translation = this.translations[this.currentLang]?.[key];
    if (translation) return translation;
    
    // Fallback to French
    if (this.currentLang !== "fr") {
      translation = this.translations.fr?.[key];
      if (translation) return translation;
    }
    
    // Return fallback or key
    return fallback || key;
  },
  
  getAvailableLanguages: function() {
    return Object.keys(this.translations);
  },
  
  getCurrentLanguage: function() {
    return this.currentLang;
  }
